-- полное удаление прежней базы данных
DROP DATABASE IF EXISTS library_db;

-- создание новой базы данных
CREATE DATABASE library_db WITH ENCODING 'UTF8' TEMPLATE template0;

-- подключение к базе данных (psql)
\c library_db;

-- создание таблицы
CREATE TABLE books (
    id SERIAL PRIMARY KEY,
    code VARCHAR(255) NOT NULL UNIQUE,
    title VARCHAR(255) NOT NULL,
    author VARCHAR(255) NOT NULL,
    year INTEGER NOT NULL CHECK (year BETWEEN 0 AND 2027),
    amountPages INTEGER NOT NULL CHECK (amountPages > 0),
    amountLinks INTEGER NOT NULL CHECK (amountLinks >= 0),
    citationIndex REAL NOT NULL CHECK (citationIndex >= 0)
);

